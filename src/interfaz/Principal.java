    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DefaultComboBoxModel model = (DefaultComboBoxModel) cmbAlumno.getModel();
        DefaultComboBoxModel model2 = (DefaultComboBoxModel) cmbObservacion.getModel();
        for (int i = 0; i < ListadoAlumnos.alumnos.length; i++) {
            if (ListadoAlumnos.alumnos[i] != null) {
                System.out.println("Añadiendo alumno " + ListadoAlumnos.alumnos[i]);
                model.addElement(ListadoAlumnos.alumnos[i]);
                //this.cmbAlumno.addItem(alum.Alumnos[i]);
            }
        }
        for (int i = 0; i < ListadoObservacion.observaciones.length; i++) {
            if (ListadoObservacion.observaciones[i] != null) {
                System.out.println("Añadiendo la observacion " + ListadoObservacion.observaciones[i]);
                model2.addElement(ListadoObservacion.observaciones[i]);
            }
        }
        cmbAlumno.setModel(model);
        cmbObservacion.setModel(model2);

    }//GEN-LAST:event_jButton2ActionPerformed

