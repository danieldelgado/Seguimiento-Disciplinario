    private void RefreshTable(Registro obj) {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("");
        model.addColumn("     Nombre    ");
        model.addColumn("    Apellido    ");
        model.addColumn("Curso");
        model.addColumn("Division");
        model.addColumn("Edad");
        model.addColumn("Comportamiento");
        String[] fila = new String[7];
        for (int i = 0; i < ListadoAlumnos.alumnos.length; i++) {
            if (ListadoAlumnos.alumnos[i] != null) {
                fila[0] = Integer.toString(i+1);
                fila[1] = ListadoAlumnos.alumnos[i].getNombre();
                fila[2] = ListadoAlumnos.alumnos[i].getApellido();
                fila[3] = Integer.toString(ListadoAlumnos.alumnos[i].getAño());
                fila[4] = ListadoAlumnos.alumnos[i].getCurso();
                fila[5] = Integer.toString(ListadoAlumnos.alumnos[i].getEdad());
                fila[6] = ListadoAlumnos.alumnos[i].getComportamiento();
                //for (int j = 0; j < ListadoAlumnos.getLista()[i].observaciones.length; j++) {
                  //  fila[7] = ListadoAlumnos.alumnos[i].getObservaciones();
                //}
                model.addRow(fila);
            }
        }
        obj.tblRegistro.setModel(model);
    }

